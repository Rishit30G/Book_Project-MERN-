//! What is Express ? 
// Express is a framework for building web applications on top of Node.js.
// It simplifies the server creation process that is already available in Node.
// It provides additional features to web applications like support for templates, middleware, etc.
// app.use(express.json()); is a middleware function in Express. It parses incoming requests with JSON payloads and is based on body-parser.

// It looks at requests where the Content-Type header matches the type option, which is application/json by default.
// If the Content-Type of the incoming request matches, this middleware parses the body of the request and creates a req.body object containing the parsed data.

//! What is Node.js ?
// Node.js is an open-source and cross-platform JavaScript runtime environment.
// It is a popular tool for almost any kind of project!
// It is built on Chrome's V8 JavaScript engine and allows you to run JavaScript on the server.

//! What is React ? 
// React is a JavaScript library for building user interfaces.
// It is the view layer for web applications.
// It is not a framework. It is not a complete solution and you will often need to use more libraries with React to form a solution.


//! What is CORS ?
// CORS stands for Cross-Origin Resource Sharing. It allows us to make requests from one domain to another domain.

//! What is Mongoose ? 
// Mongoose is an Object Data Modeling (ODM) library for MongoDB and Node.js.
// It manages relationships between data, provides schema validation, and is used to translate between objects in code and the representation of those objects in MongoDB.

//! What is MongoDB ?
// MongoDB is a NoSQL database that stores data in JSON-like documents.
// It is a document database that stores data in flexible, JSON-like documents.
// It is a distributed, document-oriented database that is written in C++.

//! What is Promise ? 
// The Promise object represents the eventual completion (or failure) of an asynchronous operation and its resulting value.

//! What are CRUD Operations ? 
// CRUD stands for Create, Read, Update, and Delete. These are the basic operations that a database server performs.
// Create: Create a new record in the database.
// Read: Read the record from the database.
// Update: Update the record in the database.
// Delete: Delete the record from the database.
// GET, POST, PUT, and DELETE are the HTTP methods that are used to perform CRUD operations.

//! What is Vite ?
// Vite is a build tool that focuses on frontend development. It is a fast and lightweight alternative to webpack.
// It provides features like hot module replacement, fast builds, and instant server start.
// It also provides support for TypeScript, JSX, CSS Modules, and more.
// It is a great tool for building Vue.js and React.js applications.


//! useState 
// The useState hook is used to add state to functional components.
// It takes the initial state as an argument and returns an array of two elements.
// The first element is the current state value and the second element is a function to update the state value.

//! useEffect
// The useEffect hook is used to perform side effects in functional components.
// It takes a function as an argument and runs the function after every render.
// It is similar to componentDidMount, componentDidUpdate, and componentWillUnmount lifecycle methods in class components.

